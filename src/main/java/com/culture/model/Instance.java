	public void setAesthetic_desc(String aesthetic_desc) {
		this.aesthetic_desc = aesthetic_desc;
	}

	public String getIdentifier() {
		return identifier;
	}

	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	
	
	
